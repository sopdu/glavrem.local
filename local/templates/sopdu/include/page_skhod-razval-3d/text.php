<p>
    <strong>Проверка и регулировка углов установки колес</strong> (в обиходе сход-развал) необходимы для обеспечения наилучшей устойчивости и управляемости автомобиля, а также равномерного износа шин при его эксплуатации.
</p>
<p>
    Периодически при эксплуатации авто их полезно контролировать. В случае, если на машине были заменены отдельные элементы подвески или происходили серьезные удары по ходовой части это необходимо делать сразу.
</p>